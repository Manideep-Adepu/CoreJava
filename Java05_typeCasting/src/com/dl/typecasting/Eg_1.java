package com.dl.typecasting;

public class Eg_1 {

	public static void main(String[] args) {
		byte a = 1;
		int b = a;
		short c = a;
		long d = c;
		float f = d;
		double e = f;
		
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);

	}

}
